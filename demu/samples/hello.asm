HWN A

SET A, 0x0
SET B, 0x8000
HWI 0x0

SET I, 0x0

CLEAR:
	SET [0x8000+I], 0x0f20
	ADD I, 0x0001
	IFN I, 0x0180
		SET PC, CLEAR

SET I, 0x0

PRINT:
	SET A, HELLO
	ADD A, I
	SET A, [A]

	IFE A, 0x0
		SET PC, HANG

	BOR A, 0x0F00
	SET B, 0x8000
	ADD B, I
	SET [B], A
	ADD I, 0x1

	SET PC, PRINT

HANG:
	SET PC, HANG

	HELLO:
		DAT "Hello, World!", 0x0000
